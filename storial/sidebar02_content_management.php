               <p><b>3rd Party Content Management Systems</b></p>
               <p>Out-of-the-box content management systems can be utilized to manage your website as well. Since 1999
                  Storiale Consulting Group has implemented and customized out-of-the-box content management systems,
                  portals and open-source applications.</p>
               <p><b>Serena Collage</b> <br>An enterprise content management system used on tens of thousands of
                  websites, Storiale Consulting Group managed the Chicago Public Library website in 2009.</p>
               <p><b>Denali</b> <br>A proprietary content management system implemented by High Point Solutions in 2003.
                  Denali was a content management system that was used to create the award winning FASTENnetwork.org, a
                  site managed by Storiale Consulting Group from 2003-2007.</p>
               <p><b>WordPress</b> <br>An open source content management system first popularized as a blogging
                  platform. Wordpress has evolved into one of the most utilized content management systems for small
                  business websites. Its open source status has allowed thousands of contributor to produce some of the
                  best and easiest plugins for content management, e-commerce, mailing lists, testimonials, captcha
                  applications and much more. Storiale Consulting Group has created several websites using WordPress
                  including a realtor website for Mike Tootelian that made it to Google's first page, first result when
                  searching "bucktown realtor." This listing made it to the top-spot in just 4 weeks.</p>
               <p><b>Joomla</b> <br>This content management system was used by the Professional Education Institute for
                  the carletonsheets website, which Lou Storiale managed in 2007. Joomla was used for the company
                  website, e-commerce, blog, podcast, articles, and landing pages. Joomla is one of the most used
                  content management systems in the United States.</p>
               <p><b>Drupal</b> <br>One of the main competitors of Joomla, Drupal, is best known for its use in
                  high-content websites and is used by many public libraries. Another open source platform, Drupal is
                  seen as easier to use than Joomla by many critics. Drupal should be considered as a content management
                  option for high-content websites only.</p>
               <p><b>SilverStripe</b> <br>An up-and-coming content management system that began as a proprietary CMS,
                  but was released as open source recently. SilverStripe was used on the Sonia Roselli website in 2010.
                  SilverStipe is highly customizable and scalable. SilverStripe is the preferred out-of-the-box content
                  management system by Storiale Consulting Group.</p>
               <p><b>WebCT</b> <br>A learning management system used to produce almost every University online course
                  from 1998-2007, WebCT was purchase by BlackBoard and remains one of the most utilized learning
                  management systems in the U.S. Lou Storiale utilized this LMS for many of the online courses for the
                  Pharmaceutical Education and Research Institute from 1999-2001.</p>
               <p><b>WebMentor</b> <br>An award winning learning management system, WebMentor is highly customizable
                  even though it is a proprietary system. Government, military, university and private company online
                  training systems have been created using WebMentor. Lou Storiale used WebMentor in the creation of 2
                  online courses for the Pharmaceutical Education and Research Institute in 2001.</p>
               <p><b>Aqualogic Portals</b> <br>AquaLogic comprised a software suite developed by BEA Systems for
                  managing service-oriented architecture (SOA). Following the acquisition of BEA by Oracle Corporation
                  in April 2008, most of the software have been renamed and the term AquaLogic is not used in any new
                  Oracle product. However, this portal system was managed by Lou Storiale from 2007-2008 during his time
                  with the Association of Legal Administrators (ALA). ALA used this system to operate and managed their
                  member portal.</p>
               <p>Known for its difficulty in customization, proprietary licensing and expensive updates, Lou Storiale
                  created a plan to create a custom portal in 2008. ALA programmers launched a customized portal in 2010
                  which will save the organization hundreds of thousands of dollars in management and hosting fees over
                  the next 5 years.</p>
